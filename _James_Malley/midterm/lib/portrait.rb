class Portrait
	attr_accessor :weather
	def initialize(weather)
		@weather = weather
	end
	def rain
		weather_pic = {
			"line1" => ['=','=','=','=','=','=','=','=','=','=','=','=','=','=','=','=','=','=','=','='],
			"line2" => ['/','/','/','/','/','/','/','/','/','/','/','/','/','/','/','/','/','/','/','/'],
			"line3" => ['/','/','/','/','/','/','/','/','/','/','/','/','/','/','/','/','/','/','/','/'],
			"line4" => ['/','/','/','/','/','/','/','/','/','/','/','/','/','/','/','/','/','/','/','/'],
			"line5" => ['/','/','/','/','/','/','/','/','/','/','/','/','/','/','/','/','/','/','/','/'],
			"line6" => ['/','/','/','/','/','/','/','/','/','/','/','/','/','/','/','/','/','/','/','/'],
			"line7" => ['_','_','_','_','_','_','_','_','_','_','_','_','_','_','_','_','_','_','_','_']
		}
		colorize_lines(weather_pic, :light_black, :cyan, :blue)
	end

	def clouds
		weather_pic = {
			"line1" => ['~','_','~','_','_','~','~','~','_','_','_','_','_','_','~','~','~','_','_','~'],
			"line2" => ['(','_','(','_','_',')',' ','(','_','_',')','_','_','_',')',' ','(','_','_',')'],
			"line3" => ['~','~','~','~','~'.colorize(:blue),'~','~','~','~','~','~','~','~','~','~','~','~','~','~','~'],
			"line4" => [''],
			"line5" => [''],
			"line6" => [''],
			"line7" => ['_','_','_','_','_','_','_','_','_','_','_','_','_','_','_','_','_','_','_','_']
		}
		colorize_lines(weather_pic, :light_black, :light_black, :light_white)
	end

	def clear
		weather_pic = {
			"line1" => ['=','=','=','=','=','=','=','=','=','=','=','=','=','=','=','=','=','=','=','='],
			"line2" => [''],
			"line3" => [''],
			"line4" => [''],
			"line5" => [''],
			"line6" => [''],
			"line7" => ['_','_','_','_','_','_','_','_','_','_','_','_','_','_','_','_','_','_','_','_']
		}
		colorize_lines(weather_pic, :blue, :green, :light_white)
	end

	def colorize_lines(art, top, bottom, middle)
		art.each do |k,v|
			# window frame # v[9] = "||"
			if k == "line1"
				puts v.join('').colorize(top)
			elsif k == "line7"
				puts v.join('').colorize(bottom)
			else
				puts v.join('').colorize(middle)
			end
		end
	end
end